public class IfConditionalClass {
    public static void main(String[] args) {
        int age = 18;
        
        if (age <= 5){
        System.out.println("Bayi"); 
        }
        else if (age <= 12){
        System.out.println("Anak-anak");  
        }
        else if (age <= 21){
        System.out.println("Remaja");  
        }
        else {
          System.out.println("Orang tua");   
        }
    }
}
